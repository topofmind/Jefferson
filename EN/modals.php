				<h3>Huerta Escolar</h3>
				<p>“Our Vegetable Garden” is the main learning-teaching project for the Natural Science and Environmental Education class in second grade. This project provides a real-life meaningful context which helps build their understanding of scientific concepts and allows students to develop scientific skills as naturalists. It also allows children to grow sensitive towards their natural surroundings and strengthen values like respect and responsibility concerning oneself and the group. In this project subjects such as Social Studies, Math, English Technology P.E. and Art work together making valuable transformations in every student regarding their being.</p>

				<p>The experiences provided in the vegetable garden enable children to learn in a meaningful way about plant’s life cycle and the relationship among them; just as the relationship of other living things with their surroundings. Exploring, observing, investigating and experimenting in this outdoor laboratory enriches an active learning process. Students discover the necessary conditions to set a garden, they identify different types of soil, they learn to prepare a fertile soil for planting a variety of seeds (which have been carefully observed in class to learn about their physical and internal characteristics in previous classes) and they are witness of the wonderful growing process plants go through. Moreover, they can observe some of the visible interactions among plants, insects and other organisms, which helps them understand this natural balance and the importance of protecting it.</p>

				<p>Children write down they experience in a very detailed way in their science sketchbook. They use illustrations, graphic organizers, individual written texts and collective texts. As part of the group work, they discuss and analyze their notes, and they draw conclusions. This collective work is a valuable learning strategy. At the same time they write, read and analyze different types of texts: scientific-informative texts, lyric texts, narrative texts and audiovisual texts that broaden their knowledge and sensibility towards nature.</p>

				<p>Finally, being responsible for the vegetable garden helps them work together as a team, helps them find solutions to difficulties that arise in the everyday practice and helps them savor the achievements they harvest.</p>

				<p class="autor"><b>Written by Aura Elena Mendoza (Natural Sciences Advisor)</b></p>

