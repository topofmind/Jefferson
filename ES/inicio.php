	let closeModal = document.getElementById('closeModal');
	let modal = document.getElementById('modal');
	let contModal = document.getElementById('contModal');

	//---------------- MODAL ADVANCED ------------------------

	function interactiveModal(btn,modal,closeModal,contModal){

		btn.onclick = ()=>{

				modal.classList.add("visible");
				contModal.classList.add("fadeUp");
			}


		closeModal.onclick = () =>{
			modal.classList.remove("visible");
		}

	}

	interactiveModal(advanded,modal,closeModal,contModal)

	//------------------- CALENDARIO --------------------

	calendar.onclick = ()=>{
		conttCalendar.className = "conttCalendar on";
		heroCalendar.className = "hero-calendar on";
	}

	closeCalendar.onclick= ()=>{
		heroCalendar.className = "hero-calendar off";
		conttCalendar.className = "conttCalendar off";
	}

	//--------.------------------------------------------
	
	let contador = 0;

	function tamaño(){

		contBanner.style.height = contBanner.offsetWidth * 0.3+"px"

		window.addEventListener('resize',function(){

			if (window.innerWidth <= 600) {
				console.log('size')
				contBanner.style.height = "auto";
