				<h3>Huerta Escolar</h3>
				<p>El proyecto “Nuestra Huerta Escolar” es el centro de enseñanza-aprendizaje de las Ciencias Naturales y Educación Ambiental en segundo de primaria. Es un contexto real y significativo que favorece la adquisición de conocimientos en ciencias naturales, el desarrollo de habilidades como científicos naturales; permite la sensibilización frente al entorno natural y fortalece valores como el respeto y la responsabilidad personal y grupal. En este proyecto se integran las ciencias sociales, matemáticas, inglés, tecnología, educación física y artes plásticas, logrando transformaciones muy valiosas del ser.</p>

				<p>Las experiencias de aprendizaje que se dan en la huerta permiten que los niños aprendan de una manera vivencial acerca del ciclo de vida de las plantas y las interrelaciones que se dan entre éstas, otros seres vivos y su entorno físico. Las actividades de exploración, observación, indagación y experimentación en este laboratorio al aire libre dinamizan procesos de aprendizaje activo: descubren cuáles son las condiciones necesarias para sembrar una huerta, identifican diferentes tipos de suelos, aprenden cómo preparar el suelo fértil para sembrar diferentes clases de semillas (las cuales observan en detalle previamente para conocer sus características físicas externas e internas) y son testigos de los diferentes momentos del desarrollo de las plantas. Además pueden examinar algunas interacciones entre las plantas, los insectos y otros organismos, lo que les posibilita reconocer el equilibrio que existe en la naturaleza y la importancia de protegerlo.</p>

				<p>Los niños registran todas las experiencias de manera muy detallada en sus bitácoras de ciencias por medio de ilustraciones, gráficos y textos escritos elaborados individual y grupalmente. En grupo analizan estos registros y los discuten, lo que los lleva a elaborar conclusiones, una forma muy valiosa de aprendizaje colectivo. De manera paralela, escriben, leen y analizan diferentes tipos de textos: informativos-científicos, líricos, narrativos y audiovisuales que amplían sus conocimientos y su sensibilidad frente a la naturaleza.</p>

				<p>Finally, being responsible for the vegetable garden helps them work together as a team, helps them find solutions to difficulties that arise in the everyday practice and helps them savor the achievements they harvest.</p>

